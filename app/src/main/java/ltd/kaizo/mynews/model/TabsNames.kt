     * Top stories tabs names.
     */
    TOP_STORIES(0),
    /**
     * Most popular tabs names.
     */
    MOST_POPULAR(1),
    /**
     * Custom tab tabs names.
     */
    CUSTOM_TAB(2),
    /**
     * Search tabs names.
     */
    SEARCH(3)
}
